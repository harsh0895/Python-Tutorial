# Conditional statements in python:-

# if, elif, else
# Match case statements


age = 18;

if( age >= 18 ):
    print("You can drive!")
elif( age < 0 ):
    print("You are entering invalid age!")
else:
    print("You cannot drive!")


# Match case statement:-

days = 1;

match days:
    case 1:
        print("Monday")
    case 2:
        print("tuesday")
    case 3:
        print("wednesday")
    case 4:
        print("thursday")
    case 5:
        print("friday")
    case 6:
        print("sat")
    case 7:
        print("Sunday")




